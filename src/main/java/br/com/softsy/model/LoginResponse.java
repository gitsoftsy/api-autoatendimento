package br.com.softsy.model;

public class LoginResponse {
	
	private String aluno;
	private String nome;	
	private String ativo;
	private String cobrancasVencer;	
	private String cobrancasVencidas;	
	private String sitAluno;	
	private String curso;
	private String docPendente;	
	private String email;
	private String celular;
	
	/**
	 * @return the aluno
	 */
	public String getAluno() {
		return aluno;
	}
	/**
	 * @param aluno the aluno to set
	 */
	public void setAluno(String aluno) {
		this.aluno = aluno;
	}
	/**
	 * @return the nome
	 */
	public String getNome() {
		return nome;
	}
	/**
	 * @param nome the nome to set
	 */
	public void setNome(String nome) {
		this.nome = nome;
	}
	/**
	 * @return the ativo
	 */
	public String getAtivo() {
		return ativo;
	}
	/**
	 * @param ativo the ativo to set
	 */
	public void setAtivo(String ativo) {
		this.ativo = ativo;
	}
	/**
	 * @return the cobrancasVencer
	 */
	public String getCobrancasVencer() {
		return cobrancasVencer;
	}
	/**
	 * @param cobrancasVencer the cobrancasVencer to set
	 */
	public void setCobrancasVencer(String cobrancasVencer) {
		this.cobrancasVencer = cobrancasVencer;
	}
	/**
	 * @return the cobrancasVencidas
	 */
	public String getCobrancasVencidas() {
		return cobrancasVencidas;
	}
	/**
	 * @param cobrancasVencidas the cobrancasVencidas to set
	 */
	public void setCobrancasVencidas(String cobrancasVencidas) {
		this.cobrancasVencidas = cobrancasVencidas;
	}
	/**
	 * @return the sitAluno
	 */
	public String getSitAluno() {
		return sitAluno;
	}
	/**
	 * @param sitAluno the sitAluno to set
	 */
	public void setSitAluno(String sitAluno) {
		this.sitAluno = sitAluno;
	}
	/**
	 * @return the curso
	 */
	public String getCurso() {
		return curso;
	}
	/**
	 * @param curso the curso to set
	 */
	public void setCurso(String curso) {
		this.curso = curso;
	}
	/**
	 * @return the docPendente
	 */
	public String getDocPendente() {
		return docPendente;
	}
	/**
	 * @param docPendente the docPendente to set
	 */
	public void setDocPendente(String docPendente) {
		this.docPendente = docPendente;
	}
	/**
	 * @return the email
	 */
	public String getEmail() {
		return email;
	}
	/**
	 * @param email the email to set
	 */
	public void setEmail(String email) {
		this.email = email;
	}
	/**
	 * @return the celular
	 */
	public String getCelular() {
		return celular;
	}
	/**
	 * @param celular the celular to set
	 */
	public void setCelular(String celular) {
		this.celular = celular;
	}
	
}
